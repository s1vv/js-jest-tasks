// Задача 1: Возведение числа в квадрат, объявите фунцию squareNumber
function squareNumber(num) {
  return num * num;
}

// Задача 2: Удвоение числа, объявите фунцию doubleNumber
function doubleNumber(num) {
  return num * 2;
}

// Задача 3: Увеличение числа на 1, объявите фунцию incrementNumber
function incrementNumber(num) {
  return num + 1;
}


module.exports = { squareNumber, doubleNumber, incrementNumber };
